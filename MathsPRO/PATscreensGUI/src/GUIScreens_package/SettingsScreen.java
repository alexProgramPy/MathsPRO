    private void showNotificationManagementPanel() {
        if (notificationPanel != null) {
            // Set initial values from existing preferences (optional)
            NotificationPreferences preferences = new NotificationPreferences();
            notificationPanel.setInitialValues(preferences);

            // Create a dialog to show the panel
            JDialog dialog = new JDialog(this, "Notification Preferences", true); // true for modal
            dialog.setLayout(new BorderLayout());

            dialog.add(notificationPanel, BorderLayout.CENTER);

            JButton closeButton = new JButton("Close");
            closeButton.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialog.setVisible(false);
                    dialog.dispose(); // Close the dialog when the button is clicked

                    jPanel1.remove(notificationPanel);
                    jPanel1.validate();
                    jPanel1.repaint();

                    getContentPane().remove(notificationPanel);
                    validate();
                    repaint();
                }
            });
            dialog.add(closeButton, BorderLayout.SOUTH);
            // Sets the dialog properties
            //Sets the size of the dialog box
            dialog.setSize(300, 200);
            dialog.setLocationRelativeTo(this); // Center dialog relative to the SettingsScreen
            dialog.setVisible(true); // Show the dialog
        } else {
            System.out.println("Notification panel is not initialized correctly");
        }
    }
