    //Save preferences to a file
    private void saveToFile(String text) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("savedName.txt"))) {
            writer.write(text);
            JOptionPane.showMessageDialog(this, "Your name/alias was saved successfully!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving name: " + e.getMessage());
        }
    }

    private void saveToFile1(String text1) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("savedEmail.txt"))) {
            writer.write(text1);
            JOptionPane.showMessageDialog(this, "Your Email was saved successfully!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving Email: " + e.getMessage());
        }
    }

